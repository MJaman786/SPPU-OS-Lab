Linux Commands : 
	
	1.To set a path 
		cd <folder name> example,
window@window-virtual-machine:~$ cd Desktop
		
	2.To Create a folder
		mkdir <folder name> example,
window@window-virtual-machine:~/Desktop$ mkdir folder

	3.To move in created folder
		cd <created_folder_name> example,
window@window-virtual-machine:~/Desktop$ cd folder

	4.To return to previous folder or path
		cd .. example,
window@window-virtual-machine:~/Desktop/folder$ cd ..

	5.To remove the folder or file
		rm -r file/folder name example,
window@window-virtual-machine:~/Desktop$ sudo su
[sudo] password for window: 
root@window-virtual-machine:/home/window/Desktop# rm -r folder

	6.To list the items in folder
		ls example
window@window-virtual-machine:~/Desktop/aman$ ls
